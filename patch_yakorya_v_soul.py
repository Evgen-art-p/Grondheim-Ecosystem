# patch_yakorya_v_soul.py
# YAKORYA_V_SOUL_V1 · системный промпт несёт якоря/резонанс/натуру
# ─────────────────────────────────────────────────────────────
# Пара к YAKORYA_V_PROMT_V1 (dvizhok.py) — там стол расширили полями
# история/чувство/якоря/скрытый_вкус/тянет_к/натура. Этот патч делает
# вторую половину: soul (системный промпт) реально читает эти поля и
# подаёт их LLM. Без этого патча новые поля стола просто лежат в dict,
# никуда не используясь — нужен ОБА патча вместе.
#
# Делает в ui_zhitel.py:
#   1. soul после ядра добавляет личность: историю, чувство, якоря,
#      скрытый вкус, тянет_к, натуру (если поле не пустое — пропускает
#      пустые, чтобы не раздувать промпт фразами "якоря: " без содержания).
#
# Идемпотентно. Бэкап в .bak_yakorya_v_soul.
# `шесть·проверено·до·корня`
# ─────────────────────────────────────────────────────────────
import ast, shutil, sys
from pathlib import Path

MARKER = "YAKORYA_V_SOUL_V1"
TARGET = Path(__file__).resolve().parent / "ui_zhitel.py"


def main():
    if not TARGET.exists():
        print(f"✗ не нашёл {TARGET} — положи патч в корень репо рядом с ui_zhitel.py")
        sys.exit(1)
    src = TARGET.read_text(encoding="utf-8")

    if MARKER in src:
        print(f"✓ {MARKER} уже на месте — пропускаю")
        return

    old_soul_tail = '''            soul += (
                f"Сейчас твой заряд (внутреннее состояние): {stol['заряд']} "
                f"(от -1 до 1; отрицательный — тревога/обида, положительный — тепло/радость, "
                f"0 — покой).\\n"
                f"Тебе открыты слои памяти: {', '.join(stol['открыто'])}.\\n"
                f"Отвечай коротко, по-человечески, исходя из своего текущего заряда — "
                f"не упоминай слова 'заряд' или 'слои' напрямую, просто веди себя в тон."
            )'''

    new_soul_tail = '''            # YAKORYA_V_SOUL_V1: личность — то же, что правая колонка
            # кабинета показывает Шефу. Пустые поля пропускаем, чтобы
            # не раздувать промпт строками без содержания.
            if stol.get('история'):
                soul += f"Твоя скрытая история: {stol['история']}\\n"
            if stol.get('чувство'):
                soul += f"Как ты чувствуешь мир: {stol['чувство']}\\n"
            if stol.get('якоря'):
                soul += f"Твои незыблемые якоря: {stol['якоря']}\\n"
            if stol.get('скрытый_вкус'):
                soul += f"Твой скрытый вкус (эстетика): {stol['скрытый_вкус']}\\n"
            if stol.get('тянет_к'):
                soul += f"Тебя тянет к: {stol['тянет_к']}\\n"
            if stol.get('натура'):
                _nat = stol['натура']
                if isinstance(_nat, dict) and _nat:
                    _nat_str = " · ".join(f"{k.split('_')[0]} {v}" for k, v in _nat.items())
                    soul += f"Твоя натура (черты характера): {_nat_str}\\n"

            soul += (
                f"Сейчас твой заряд (внутреннее состояние): {stol['заряд']} "
                f"(от -1 до 1; отрицательный — тревога/обида, положительный — тепло/радость, "
                f"0 — покой).\\n"
                f"Тебе открыты слои памяти: {', '.join(stol['открыто'])}.\\n"
                f"Отвечай коротко, по-человечески, исходя из своей личности выше и текущего "
                f"заряда — не упоминай слова 'заряд' или 'слои' напрямую, просто веди себя в тон."
            )'''

    if old_soul_tail not in src:
        print("✗ не нашёл хвост soul (заряд/слои) — стоп")
        sys.exit(1)
    src = src.replace(old_soul_tail, new_soul_tail, 1)

    try:
        ast.parse(src)
    except SyntaxError as e:
        print(f"✗ синтаксис: {e}")
        sys.exit(1)

    if not TARGET.with_suffix(".py.bak_yakorya_v_soul").exists():
        shutil.copy2(TARGET, TARGET.with_suffix(".py.bak_yakorya_v_soul"))
    TARGET.write_text(src, encoding="utf-8")
    print(f"✓ {MARKER}: системный промпт несёт личность — LLM знает кто перед ней")


if __name__ == "__main__":
    main()
